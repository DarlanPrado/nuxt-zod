import { existsSync, readdirSync, statSync } from 'node:fs'
import { join, normalize, relative, sep } from 'node:path'

export interface SchemaFileEntry {
  absolutePath: string
  segments: string[]
}

type TreeNode
  = { kind: 'leaf', importId: string }
    | { kind: 'branch', children: Record<string, TreeNode> }

/** kebab/snake segment → valid JS property key (camelCase); prefixes `_` if starts with a digit */
export function fileSegmentToPropertyKey(segment: string): string {
  const camel = segment
    .replace(/^[.-]+/, '')
    .replace(/[-_]+([a-z0-9])/gi, (_, c: string) => c.toUpperCase())
  let key = camel.length > 0 ? camel : segment
  if (/^\d/.test(key))
    key = `_${key}`
  if (!/^[$A-Z_][\w$]*$/i.test(key)) {
    throw new Error(
      `nuxt-zod: schema file path segment "${segment}" is not a valid JavaScript identifier after normalization; rename the file or directory`,
    )
  }
  return key
}

function collectTsFiles(absoluteDir: string, out: string[] = []): string[] {
  if (!existsSync(absoluteDir))
    return out
  for (const name of readdirSync(absoluteDir)) {
    if (name === 'index.ts')
      continue
    const full = join(absoluteDir, name)
    const st = statSync(full)
    if (st.isDirectory()) {
      collectTsFiles(full, out)
    }
    else if (name.endsWith('.ts')) {
      out.push(full)
    }
  }
  return out
}

export function discoverSchemaFiles(schemasDirAbsolute: string): SchemaFileEntry[] {
  const files = collectTsFiles(schemasDirAbsolute).sort((a, b) => a.localeCompare(b))
  const entries: SchemaFileEntry[] = []
  const root = schemasDirAbsolute.replace(/[/\\]+$/, '')
  for (const absolutePath of files) {
    const rel = relative(root, absolutePath).split(sep).join('/')
    const withoutExt = rel.replace(/\.ts$/, '')
    const rawSegments = withoutExt.split('/').filter(Boolean)
    const segments = rawSegments.map(fileSegmentToPropertyKey)
    entries.push({ absolutePath, segments })
  }
  return entries
}

function setPathAtRoot(root: Record<string, TreeNode>, segments: string[], importId: string) {
  let level: Record<string, TreeNode> = root
  for (let i = 0; i < segments.length; i++) {
    const seg = segments[i]!
    const isLast = i === segments.length - 1
    if (isLast) {
      if (level[seg]?.kind === 'branch') {
        throw new Error(
          `nuxt-zod: cannot register schema at "${segments.join('.')}": a subdirectory already uses that name`,
        )
      }
      level[seg] = { kind: 'leaf', importId }
      return
    }
    const existing = level[seg]
    if (existing?.kind === 'leaf') {
      throw new Error(
        `nuxt-zod: cannot register nested schema under "${segments.slice(0, i + 1).join('.')}": a file already exists with that name`,
      )
    }
    if (!existing) {
      level[seg] = { kind: 'branch', children: {} }
    }
    const branch = level[seg] as Extract<TreeNode, { kind: 'branch' }>
    level = branch.children
  }
}

function emitObjectLevel(level: Record<string, TreeNode>): string {
  const keys = Object.keys(level).sort((a, b) => a.localeCompare(b))
  const parts: string[] = []
  for (const key of keys) {
    const node = level[key]!
    if (node.kind === 'leaf') {
      parts.push(`${key}: ${node.importId}`)
    }
    else {
      parts.push(`${key}: ${emitObjectLevel(node.children)}`)
    }
  }
  return `{ ${parts.join(', ')} }`
}

export interface ImportPathContext {
  srcDir: string
  rootDir: string
}

export function isUnderDirectory(dir: string, filePath: string): boolean {
  const d = normalize(dir)
  const f = normalize(filePath)
  return f === d || f.startsWith(d.endsWith(sep) ? d : d + sep)
}

function toImportSpecifier(ctx: ImportPathContext, absoluteFile: string): string {
  const { srcDir, rootDir } = ctx

  let rel = relative(srcDir, absoluteFile).split(sep).join('/').replace(/\.ts$/, '')
  if (rel && !rel.startsWith('..')) {
    return `~/${rel}`
  }

  const sharedAbs = join(rootDir, 'shared')
  if (isUnderDirectory(sharedAbs, absoluteFile)) {
    const fromShared = relative(sharedAbs, absoluteFile).split(sep).join('/').replace(/\.ts$/, '')
    return `#shared/${fromShared}`
  }

  rel = relative(rootDir, absoluteFile).split(sep).join('/').replace(/\.ts$/, '')
  if (rel && !rel.startsWith('..')) {
    return `~/${rel}`
  }

  throw new Error(
    `nuxt-zod: cannot build import path for schema file — keep schemas under srcDir, project root, or shared/ (${absoluteFile})`,
  )
}

export function generateUseZodSchemasSource(
  entries: SchemaFileEntry[],
  importCtx: ImportPathContext,
): string {
  if (entries.length === 0) {
    return `// Generated by nuxt-zod — do not edit manually
export function useZodSchemas() {
  return {}
}
`
  }
  const lines: string[] = [
    '// Generated by nuxt-zod — do not edit manually',
  ]
  const root: Record<string, TreeNode> = {}
  entries.forEach((entry, index) => {
    const importId = `_nzs${index}`
    const spec = toImportSpecifier(importCtx, entry.absolutePath)
    lines.push(`import ${importId} from '${spec}'`)
    setPathAtRoot(root, entry.segments, importId)
  })
  lines.push('')
  lines.push(`export function useZodSchemas() {
  return ${emitObjectLevel(root)}
}`)
  lines.push('')
  return lines.join('\n')
}
