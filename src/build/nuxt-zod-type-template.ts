/**
 * Source for `addTypeTemplate({ filename: 'types/nuxt-zod.d.ts' })` in the module setup.
 * Keeps `module.ts` readable; version-specific bits are only `zodSpecifier` + `nuxtZodProviderId`.
 */
export interface NuxtZodTypeTemplateOptions {
  /** `zod/v3` or `zod/v4` — must match Nitro virtual `#nuxt-zod/server`. */
  zodSpecifier: string
  zodVersion: 'v3' | 'v4'
}

export function getNuxtZodTypeTemplateContents(options: NuxtZodTypeTemplateOptions): string {
  const { zodSpecifier, zodVersion } = options
  return `// Auto-generated by nuxt-zod
import type { z } from '${zodSpecifier}'
import type { ZodErrorMessages } from 'nuxt-zod'

declare module '#app' {
  interface NuxtApp {
    $zod: typeof z
  }
}

declare module 'vue' {
  interface ComponentCustomProperties {
    $zod: typeof z
  }
}

declare module '#nuxt-zod/server' {
  export { z } from '${zodSpecifier}'
  export const nuxtZodProviderId: '${zodVersion}'
  export type { ValidationSchema, ValidationSchemaInput, ValidationOptions, InferValidated, NuxtZodRuntimeValidation } from 'nuxt-zod'
}

declare module 'nuxt/schema' {
  interface AppConfigInput {
    zod?: {
      errors?: ZodErrorMessages
    }
  }
  interface AppConfig {
    zod?: {
      errors?: ZodErrorMessages
    }
  }
}

export {}
`
}
